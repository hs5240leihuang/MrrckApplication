package com.meiku.dev.bean;

public class PopupData {

	String name;

	int imgRes;

	public PopupData(String name, int imgRes) {
		this.name = name;
		this.imgRes = imgRes;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public int getImgRes() {
		return imgRes;
	}

	public void setImgRes(int imgRes) {
		this.imgRes = imgRes;
	}
	
	
}
